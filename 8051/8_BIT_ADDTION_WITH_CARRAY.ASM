
ORG 00H
MOV R0,#50H // Ro <- 50 FRIST NUMBER
MOV R1,#52H // R1 <- 53 RESULT MEMORY LOCATION
//MOV R2,#52H // Ro <- 52 RESULT MEMORY LOCATION
//MOV R3,#53H // Ro <- 53 CARRAY MEMORY LOCATION


// NOTE : @ WORKS ONLY WITH R0 AND R1 AND NO TRANSFER BETWEEN TWO REGISTER

MOV R2,#00H // Ro <- 00 CARRAY VARIABLE
MOV A,@R0 // A <- [R0] 
INC R0
ADD A,@R0 //   A <- [R1] + A
JNC NEXT
MOV R2,#01H // CARRAY IN R2
NEXT:	MOV @R1,A // [R1] <- A RESULT TO MEMORY
		INC R1
		MOV A,R2
		MOV @R1,A // CARRAY TO MEMORY
		
		
END