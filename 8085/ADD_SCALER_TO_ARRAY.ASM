
LXI H,F000H ;MEM LOCATION OF ARRAY
LXI D,F010H	;MEM LOCATION OF RESULT ARRAY

MVI B,02H	;SCALER TO BE ADDED
MVI C,04H	;COUNTER 

LOOP:	MOV A,M ;ELEMENT FORM MEMORY
		INX H	;POINT TO NEXT ARRAY
        ADD B	;ADD THE SCALER
        XCHG 	; MAKE POINTER TO POINT FINAL ARRAY
        MOV M,A	;MOVE THE RESULT TO RESULT ARRAY
        INX H	;INCREMENT THE RESULT ARRAY POINTER
        XCHG	;MAKE M POINTER TO ORIGINAL ARRAY
        DCR C	;DECREMENT THE COUNTER
        JNZ LOOP

hlt