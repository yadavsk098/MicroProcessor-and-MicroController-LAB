
LXI H,F000H
MVI C,04H ; TAKE IT AS I COUNTER

LOOP:	MOV D,C	 		;J START FORM I 
		BACK:	MOV B,M ;MOVE FIRST NUMBER TO B
        		INX H	; INCREMENT THE POINTER
        		MOV A,M	;MOVE SECOND NUMBER TO A
                CMP B 	;A-B > 0 THEN SKIP 
                JNC SKIP
                MOV M,B	;B IS BIGER NUMBER SO MOVE TO MEM
                DCX H   ;DECREMENT POINTER
                MOV M,A ;MOVE SMALLER NUMBER TO MEMORY
                INX H	;INCREMENT THE COUNTER TO CURRENT POISTION
                SKIP:	DCR D ;DECREMENT THE SECOND COUNTER
                		JNZ BACK
        LXI H,F000H ;RE INTIALISE TO STARTING 
        DCR C		; DECREMNT THE I COUNTER VALUE
        JNZ LOOP

HLT