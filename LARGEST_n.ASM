MVI C,06H ;COUNTER FOR SIZE OF ARRAY
LXI H,8001H ;INTIALISE H WITH STARTING ADDRESS OF ARRAY

MOV B,M ; B WITH FIRST ELEMENT CONSIDERING IT SMALLEST

LOOP:	MOV A,M ; MEMORY TO ACCUMULATOR
        CMP B ; A-B < 0 THAT IS CARRAY (B>A)
        JC LABEL
        MOV B,A ; IF CARRAY IS GENRATED (A<B) 
        LABEL:	DCR C ; DECREMENT COUNTER
        INX H ;POINTING TO SECOND ELEMENT OF ARRAY
        JNZ LOOP   

hlt